package ksnoop

// THIS CODE MUST BE REFACTORED TO AVOID CODE DUPLICATION

import (
	"bytes"
	"context"
	"encoding/binary"
	"errors"
	"fmt"

	"github.com/cilium/ebpf/link"
	"github.com/cilium/ebpf/perf"
	"github.com/cilium/ebpf/rlimit"
	devstdout "github.com/containerscrew/devstdout/pkg"
	"github.com/containerscrew/kernelsnoop/internal/alerts"
	"github.com/containerscrew/kernelsnoop/internal/ksnoop/core"
	"github.com/containerscrew/kernelsnoop/internal/ksnoop/generates"
	"github.com/containerscrew/kernelsnoop/internal/ksnoop/utils"
	"golang.org/x/sys/unix"
)

const (
	// For /usr/bin/sh or /bin/bash
	shPath   = "/usr/lib/libreadline.so.8"
	shSymbol = "readline"
	// For /usr/bin/zsh
	zshPath   = "/usr/bin/zsh"
	zshSymbol = "zleentry"
)

func ShellReadline(ctx context.Context) {
	// Retrieve the devstdout from the context
	log, _ := ctx.Value("log").(*devstdout.CustomLogger)

	// Allow the current process to lock memory for eBPF resources
	if err := rlimit.RemoveMemlock(); err != nil {
		log.Error(fmt.Sprintf("failed to remove memlock rlimit: %v. Consider using sudo or give necessary capabilities to the program", err))
	}

	// Load pre-compiled BPF programs and maps into the kernel
	objs := generates.ShellReadlineObjects{}
	if err := generates.LoadShellReadlineObjects(&objs, nil); err != nil {
		log.Error(fmt.Sprintf("failed to load BPF objects: %v", err))
	}
	defer objs.Close()

	// Link to Ureprobe for ZSH
	// Open an ELF binary and read its symbols.
	exZsh, err := link.OpenExecutable(zshPath)
	if err != nil {
		log.Error(fmt.Sprintf("opening executable: %s", err))
	}

	// Open a Uretprobe at the exit point of the symbol and attach
	// the pre-compiled eBPF program to it.
	upZsh, err := exZsh.Uretprobe(zshSymbol, objs.UretprobeZshReadline, nil)
	if err != nil {
		log.Error(fmt.Sprintf("creating uretprobe: %s", err))
	}
	defer upZsh.Close()

	// Link to Ureprobe for sh or bash
	// Open an ELF binary and read its symbols.
	exSh, err := link.OpenExecutable(shPath)
	if err != nil {
		log.Error(fmt.Sprintf("opening executable: %s", err))
	}

	// Open a Uretprobe at the exit point of the symbol and attach
	// the pre-compiled eBPF program to it.
	upSh, err := exSh.Uretprobe(shSymbol, objs.UretprobeShReadline, nil)
	if err != nil {
		log.Error(fmt.Sprintf("creating uretprobe: %s", err))
	}
	defer upSh.Close()

	// Initialize ring buffer
	events := objs.Events
	rd, err := perf.NewReader(events, 4096)
	if err != nil {
		log.Error(fmt.Sprintf("failed to create ringbuf reader: %v", err))
	}
	defer rd.Close()

	// Subscribe to signals for terminating the program.
	core.HandleSignals(log, func() {
		// Close the perf reader when the program exits.
		if err := rd.Close(); err != nil {
			log.Error("closing perf event reader: %s", err)
		}
	})

	// Log that the program is listening for events, and the event type is shell_readline
	log.Info("listening for events",
		devstdout.Argument("event", "shell_readline"),
	)

	// ShellReadlineEvent is generated by bpf2go.
	var event generates.ShellReadlineEvent
	for {
		// Read the next event from the ring buffer.
		record, err := rd.Read()
		if err != nil {
			if errors.Is(err, perf.ErrClosed) {
				return
			}
			log.Error(fmt.Sprintf("Failed to read from ringbuf: %v", err))
			continue
		}

		// If the ring buffer is full, some events were lost.
		if record.LostSamples != 0 {
			log.Warning(fmt.Sprintf("perf event ring buffer full, dropped %d samples", record.LostSamples))
			continue
		}

		// Decode the raw sample into a ShellReadlineEvent struct.
		if err := binary.Read(bytes.NewReader(record.RawSample), binary.LittleEndian, &event); err != nil {
			log.Error(fmt.Sprintf("Error decoding event: %s", err))
			continue
		}

		// Check if the command line contains "nmap"
		if bytes.Contains(event.Line[:], []byte("nmap")) {
			err := alerts.SendNotification("kernelsnoop", "Nmap was executed on the system", "warning.png")
			if err != nil {
				log.Error(fmt.Sprintf("Failed to send notification: %s", err))
			}
		}

		// Log the event with the following fields
		log.Info("new shell command",
			devstdout.Argument("pid", fmt.Sprintf("%d", event.Pid)),
			devstdout.Argument("uid", fmt.Sprintf("%d", event.Uid)),
			devstdout.Argument("user", utils.GetUsername(event.Uid)),
			devstdout.Argument("shell", unix.ByteSliceToString(event.Shell[:])),
			devstdout.Argument("command", unix.ByteSliceToString(event.Line[:])),
		)
	}
}
